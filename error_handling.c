#include <stdio.h>
#include <stdlib.h>
#include "error_handling.h"

///////////////////////////////////////////////////////////////////////////////
//handle_error
// Description: this function prints the provided error message to stderr and
//              exits the program with a non-zero status code.
// Parameter: message - string containing the error message to be displayed.
// Return: void
///////////////////////////////////////////////////////////////////////////////
void handle_error(const char *message) {
    fprintf(stderr, "Error: %s\n", message);
    exit(EXIT_FAILURE);
}